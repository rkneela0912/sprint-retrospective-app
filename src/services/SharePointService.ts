// -------------------------------------------------------------------
// Helpers: Choice + People normalization for Code Apps connector
// -------------------------------------------------------------------

type ChoiceValue = { Value?: string } | string | null | undefined;
type PersonValue = Record<string, any>;

const choice = (v: ChoiceValue): string => {
  if (!v) return "";
  if (typeof v === "string") return v;
  return v.Value ?? "";
};

const toLower = (s: string) => (s ?? "").trim().toLowerCase();

function personToDisplayName(p: any): string {
  if (!p) return "";
  return (
    p.DisplayName ??
    p.Title ??
    p.Name ??
    p.Email ??
    (typeof p === "string" ? p : "")
  );
}

function extractCandidates(result: any): any[] {
  const data = result?.data ?? result?.result ?? result;
  if (!data) return [];

  if (Array.isArray(data?.value)) return data.value;
  if (Array.isArray(data?.results)) return data.results;
  if (Array.isArray(data)) return data;
  if (typeof data === "object") return [data];

  return [];
}

/**
 * Resolves a People value using the autogenerated Get<PeopleField> operation.
 * IMPORTANT: choose exact match by Email/UPN/Claims when possible.
 */
async function resolvePersonValue(
  search: string,
  referencedKeyNormalized: "SubmittedBy" | "Owner",
  service: { getReferencedEntity: (s: string, k: string) => Promise<any> }
): Promise<PersonValue> {
  const trimmed = (search ?? "").trim();
  if (!trimmed) return {};

  const needle = toLower(trimmed);

  try {
    const res = await service.getReferencedEntity(trimmed, referencedKeyNormalized);
    const candidates = extractCandidates(res);

    if (candidates.length > 0) {
      // Prefer exact Email/UPN match
      const exact =
        candidates.find((c: any) => toLower(c?.Email) === needle) ||
        candidates.find((c: any) => toLower(c?.UserPrincipalName) === needle) ||
        candidates.find((c: any) => (toLower(c?.Claims) || "").includes(needle));

      if (exact) return exact;

      // Otherwise, fall back to first candidate
      return candidates[0];
    }
  } catch (e) {
    console.warn(`resolvePersonValue failed for ${referencedKeyNormalized}`, e);
  }

  // last-resort fallback (many connectors accept Claims+Email)
  return {
    Claims: `i:0#.f|membership|${needle}`,
    Email: needle,
    DisplayName: needle,
  };
}

