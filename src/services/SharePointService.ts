export const getRetroBoardData = async (
  sessionId: number
): Promise<RetroBoardData | null> => {
  try {
    const [sessionResult, itemResults, actionResults] = await Promise.all([
      RetroSessionsService.get(sessionId.toString()),
      RetroItemsService.getAll({
        filter: `SessionRefID eq ${sessionId}`,
        orderBy: ["ID asc"],
      }),
      ActionItemsService.getAll({
        filter: `SessionRefID eq ${sessionId}`,
        orderBy: ["ID asc"],
      }),
    ]);

    const s = sessionResult.data as any;
    if (!s) return null;

    const actionItems: ActionItem[] = ((actionResults.data as any[]) ?? []).map(
      (a: any) => ({
        id: a.ID,
        title: a.Title,
        retroItemRefId: a.RetroItemRefID,
        sessionRefId: a.SessionRefID,
        owner: personToDisplayName(a.Owner) || "",
        status: (choice(a.Status) as any) || "Open",
        targetSprint: choice(a.TargetSprint),
        carriedOverFrom: a.CarriedOverFrom ?? null,
      })
    );

    const retroItems: RetroItem[] = ((itemResults.data as any[]) ?? []).map(
      (item: any) => ({
        id: item.ID,
        title: item.Title,
        category: (choice(item.Category) as any) || "What We Liked",
        sessionRefId: item.SessionRefID,
        isAnonymous: !!item.IsAnonymous,
        submittedBy: personToDisplayName(item.SubmittedBy) || "Unknown",
        votes: item.Votes ?? 0,
        actionItems: actionItems.filter((a) => a.retroItemRefId === item.ID),
      })
    );

    return {
      session: {
        id: s.ID,
        title: s.Title,
        sprint: choice(s.Sprint),
        status: (choice(s.Status) as any) || "Draft",
        sessionDate: s.SessionDate,
      },
      liked: retroItems.filter((i) => i.category === "What We Liked"),
      missed: retroItems.filter((i) => i.category === "What We Missed"),
      learned: retroItems.filter((i) => i.category === "What We Learned"),
      appreciations: retroItems.filter((i) => i.category === "Appreciations"),
    };
  } catch (error) {
    console.error("SharePointService: Failed to fetch board data.", error);
    return null;
  }
};

