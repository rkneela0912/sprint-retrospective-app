export const getAllSessions = async (): Promise<RetroSession[]> => {
  try {
    const result = await RetroSessionsService.getAll({
      orderBy: ["SessionDate desc"],
    });

    return ((result.data as any[]) ?? []).map((r: any) => ({
      id: r.ID,
      title: r.Title,
      sprint: choice(r.Sprint),
      status: (choice(r.Status) as any) || "Draft",
      sessionDate: r.SessionDate,
    }));
  } catch (error) {
    console.error("SharePointService: Failed to fetch sessions.", error);
    return [];
  }
};

export const createSession = async (
  title: string,
  sprint: string,
  sessionDate: string
): Promise<void> => {
  await RetroSessionsService.create({
    Title: title,
    // Choice write often supports raw string, but object is safer in connector scenarios
    Sprint: { Value: sprint } as any,
    Status: { Value: "Draft" } as any,
    SessionDate: sessionDate,
  } as any);
};

export const updateSessionStatus = async (
  sessionId: number,
  status: "Draft" | "Open" | "Closed"
): Promise<void> => {
  await RetroSessionsService.update(sessionId.toString(), {
    Status: { Value: status } as any,
  } as any);
};

